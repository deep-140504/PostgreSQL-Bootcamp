-- DISTINCT FOR SELECTING DISTINCT VALUES
SELECT
	*
FROM
	MOVIES;

SELECT
	MOVIE_LANGUAGE
FROM
	MOVIES;

SELECT DISTINCT
	MOVIE_LANGUAGE
FROM
	MOVIES;

SELECT DISTINCT
	MOVIE_LANGUAGE
FROM
	MOVIES
ORDER BY
	1;

SELECT DISTINCT
	DIRECTOR_ID
FROM
	MOVIES;

SELECT DISTINCT
	MOVIE_LANGUAGE,
	DIRECTOR_ID
FROM
	MOVIES;

SELECT DISTINCT
	*
FROM
	MOVIES
ORDER BY
	MOVIE_ID;