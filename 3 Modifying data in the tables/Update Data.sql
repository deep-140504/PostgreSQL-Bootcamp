-- UPDATING DATA INTO A TABLE
SELECT
	*
FROM
	CUSTOMERS;

-- UPDATE A RECORD
-- UPDATE A COLUMN
UPDATE CUSTOMERS
SET
	EMAIL = 'a@b.com'
WHERE
	LAST_NAME = 'Dabhi';

SELECT
	*
FROM
	CUSTOMERS;

-- UPDATE MULTIPLE COLUMNS
UPDATE CUSTOMERS
SET
	EMAIL = 'a4@b.com',
	AGE = 21
WHERE
	FIRST_NAME = 'Deep';

SELECT
	*
FROM
	CUSTOMERS;

-- RETURNING TO GET INFO ON UPDATED ROWS
UPDATE CUSTOMERS
SET
	EMAIL = 'a4@b.com',
	AGE = 21
WHERE
	FIRST_NAME = 'Deep'
RETURNING
	*;

