-- TO_CHAR
SELECT
	TO_CHAR(100000, '9,99999');

SELECT
	RELEASE_DATE,
	TO_CHAR(RELEASE_DATE, 'DD-MM-YYYY'),
	TO_CHAR(RELEASE_DATE, 'DY, MM, YYYY')
FROM
	MOVIES;

SELECT
	TO_CHAR(TIMESTAMP '2020-01-01 10:30:45', 'HH24:MI:SS');

SELECT
	MOVIE_ID,
	REVENUES_DOMESTIC,
	TO_CHAR(REVENUES_DOMESTIC, '$999D99')
FROM
	MOVIES_REVENUES;

