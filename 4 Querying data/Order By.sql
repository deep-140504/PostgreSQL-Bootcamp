-- ORDER BY TO SORT RECORDS BY COLUMN NAME
SELECT
	*
FROM
	MOVIES
ORDER BY
	RELEASE_DATE ASC;

SELECT
	*
FROM
	MOVIES
ORDER BY
	RELEASE_DATE;

SELECT
	*
FROM
	MOVIES
ORDER BY
	RELEASE_DATE DESC;

SELECT
	*
FROM
	MOVIES
ORDER BY
	MOVIE_NAME ASC,
	RELEASE_DATE DESC;

-- ORDER BY TO SORT RECORDS BY ALIAS COLUMN NAME
SELECT
	FIRST_NAME AS "Name",
	LAST_NAME AS "Surname"
FROM
	ACTORS
ORDER BY
	"Name" DESC;

-- ORDER BY TO SORT RECORDS BY EXPRESSION
SELECT
	FIRST_NAME,
	LENGTH(FIRST_NAME)
FROM
	ACTORS;

SELECT
	FIRST_NAME,
	LENGTH(FIRST_NAME) AS "Length"
FROM
	ACTORS
ORDER BY
	"Length" DESC;

SELECT
	FIRST_NAME,
	LENGTH(FIRST_NAME)
FROM
	ACTORS
ORDER BY
	LENGTH(FIRST_NAME) DESC;

-- ORDER BY TO SORT RECORDS BY COLUMN NAME OR COLUMN NUMBER
SELECT
	*
FROM
	ACTORS
ORDER BY
	FIRST_NAME ASC,
	DATE_OF_BIRTH DESC;

SELECT
	FIRST_NAME,
	LAST_NAME,
	DATE_OF_BIRTH
FROM
	ACTORS
ORDER BY
	FIRST_NAME ASC,
	DATE_OF_BIRTH DESC;

SELECT
	FIRST_NAME,
	LAST_NAME,
	DATE_OF_BIRTH
FROM
	ACTORS
ORDER BY
	1 ASC,
	3 DESC;

-- ORDER BY TO SORT RECORDS WITH NULL VALUES
CREATE TABLE DEMO (NUM INT);

INSERT INTO
	DEMO
VALUES
	(1),
	(2),
	(3),
	(NULL),
	(NULL);

SELECT
	*
FROM
	DEMO;

SELECT
	*
FROM
	DEMO
ORDER BY
	NUM ASC;

SELECT
	*
FROM
	DEMO
ORDER BY
	NUM ASC NULLS LAST;

SELECT
	*
FROM
	DEMO
ORDER BY
	NUM DESC NULLS FIRST;

DROP TABLE IF EXISTS DEMO;